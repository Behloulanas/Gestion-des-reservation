    private void listeReservationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listeReservationMouseClicked
        id = parseInt(model.getValueAt(listeReservation.getSelectedRow(), 0).toString());
        clientlist.getModel().setSelectedItem(rs.findById(id).getCl());
        chambrelist.getModel().setSelectedItem(rs.findById(id).getCh());
        datedebut.setDate(rs.findById(id).getDatedebut());
        datefin.setDate(rs.findById(id).getDatefin());
    }//GEN-LAST:event_listeReservationMouseClicked

