    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
Client cls = (Client) clientlist.getSelectedItem();
        Chambre cham = (Chambre) chambrelist.getSelectedItem();
        Date db = datedebut.getDate();
        Date df = datefin.getDate();
        Reservation res = rs.findById(id);
        res.setId(id);
        res.setCl(cls);
        res.setCh(cham);
        res.setDatedebut(db);
        res.setDatefin(df);
        int reponse = JOptionPane.showConfirmDialog(this, "voulez vous Modifier");
       if (reponse == 0)
           if(rs.update(res))
               JOptionPane.showMessageDialog(this,"Reservation est bien Modifier");
       load();        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
Client cls = (Client) clientlist.getSelectedItem();
        Chambre cham = (Chambre) chambrelist.getSelectedItem();
        Date db = datedebut.getDate();
        Date df = datefin.getDate();
        if(rs.create(new Reservation(db,df,cls,cham))){
             JOptionPane.showMessageDialog(this, "Reservation est bien Ajouter");
        }
        load();       
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       int reponse = JOptionPane.showConfirmDialog(this,"voulez vous Supprimer");
        if(reponse==0)
            if(rs.delete(rs.findById(id))){
                 JOptionPane.showMessageDialog(this,"Reservation supprimé");
            load();
            loadClient();
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

